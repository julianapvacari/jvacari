#2. Write a Program that asks for a value and shows on the screen if the value is positive or negative.

def positive_or_negative 


    puts "Enters the number and I will shows up if they are positive or negative!"


    number = gets.chomp.to_f



    if (number >= 0)
        puts "The number is positive!"

    elsif (number <= -1)
        puts "The number is negative!"





    end

end

